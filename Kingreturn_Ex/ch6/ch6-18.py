# 串列刪除元素應用
nums1 = [1, 3, 5]
print("刪除nums1串列元素索引1元素前 ＝ ", nums1)
del nums1[1]
print("刪除nums1串列元素索引1元素後 ＝ ", nums1)
nums2 = [1, 2, 3, 4, 5, 6]
print("刪除nums2串列元素索引[0:2]元素前 ＝ ", nums2)
del nums2[1]
print("刪除nums2串列元素索引[0:2]元素後 ＝ ", nums2)
nums3 = [1, 2, 3, 4, 5, 6]
print("刪除nums3串列元素索引[0:6:2]元素前 ＝ ", nums3)
del nums3[1]
print("刪除nums3串列元素索引[0:6:2]元素後 ＝ ", nums3)
